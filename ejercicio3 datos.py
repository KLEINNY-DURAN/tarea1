#Dados dos (2) números calcule la suma, resta, multiplicación, división y módulo.
numero1_kmdt = int(input("Ingrese numero uno:  "))
numero2_kmdt = int(input("Ingrese numero dos:  "))
#calculo
suma_kmdt = numero1_kmdt + numero2_kmdt
resta_kmdt = numero1_kmdt - numero2_kmdt
multiplicacion_kmdt = numero1_kmdt * numero2_kmdt
division_kmdt = numero1_kmdt / numero2_kmdt
modulo_kmdt = numero1_kmdt % numero2_kmdt
print("el resultado de la suma es:" + str(suma_kmdt))
print("el resultado de la resta es:" + str(resta_kmdt))
print("el resultado de la multiplicacion es:" + str(multiplicacion_kmdt))
print("el resultado de la division es:" + str(division_kmdt))
print("el resultado del modulo es:" + str(modulo_kmdt))